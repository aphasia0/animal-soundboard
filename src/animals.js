export function getRandomAnimal(excludeId = null) {
    let availableAnimals = animals;
    if (excludeId !== null) {
        availableAnimals = animals.filter(a => a.id !== excludeId);
    }
    const randomIndex = Math.floor(Math.random() * availableAnimals.length);
    return availableAnimals[randomIndex];
}
